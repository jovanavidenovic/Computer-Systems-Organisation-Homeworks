extern volatile int long_click;
extern volatile int detect_change;
extern volatile int x_offset;
extern volatile int y_offset;
extern volatile int counter;

uint32_t time_begin = 0;
uint32_t time_end = 0;

void EXTI0_IRQHandler(){
	if(__HAL_GPIO_EXTI_GET_IT(GPIO_PIN_0) != 0 && delay == 0){
		if(counter % 2 == 0) {
			delay = 500;
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, 0);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, 0);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, 0);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, 0);
			counter ++;
			time_begin = systick_counter;
			detect_change += 1;
			detect_change %= 3;
			__HAL_GPIO_EXTI_CLEAR_FLAG(GPIO_PIN_0);
		} else {
			delay = 500;
			counter ++;
			time_end = systick_counter;
			if(time_end - time_begin > 1000){
				long_click = 1;
				delay = 500;
			}
			systick_counter = 0;
		}
	}
}
